            <h2 class="display-7=8">Amending the DA-PhilRice Charter and Reconstituting the BOT</h2>
            <div class="highlight">
                <p>
                    The amendment of the DA-PhilRice Charter and reconstitution of its Board of Trustees (BOT) are crucial steps to strengthen the institutional capacity of DA-PhilRice. These moves will enable the Institute to carry out its mandate and serve as the premier R4DE agency for food security and generate science-based innovations, helping surmount natural and man-made challenges to the country’s rice industry.
                </p>
                <p>
                    DA-PhilRice was created through Executive Order (EO) 1061 in November 1985. It steadfastly pursued its mission to help respond to the needs of Filipino rice farmers and consumers and the country’s efforts to attain food security. It generates and shares cutting-edge science innovations in various disciplines.
                </p>
                <p>
                    Even so, amidst a changing landscape and socio-economic context, formidable challenges continuously confront agriculture in general and the rice industry in particular. For one, climate change incessantly challenges productivity improvement in adverse environments such as rainfed, upland, saline, drought, and flood-prone areas. Increasing water scarcity, strong typhoons, frequent droughts, rising temperatures, new pests and diseases, shorter growing seasons, and degraded natural resources constantly threaten Filipino farmers.
                </p>
                <p>
                    These are further aggravated by socio-economic problems such as rising costs of farm inputs, labor scarcity, continuous reduction of per capita cultivated land, and more intense competition with international trade. The enactment of RA 11203 has also expanded the Institute’s mandate and area of operations, which required additional human resources with appropriate skills to deliver the expected seed-related services to farmers.
                </p>
                <p>
                    Surmounting these gargantuan tasks requires the services of highly competent and multi-disciplinary teams of agricultural and social scientists, amply supported with facilities and funds and with efficient support staff and conducive working conditions. To pursue rice R4DE programs to improve productivity, DA-PhilRice needs a strong and full-force BOT and an updated charter.
                </p>
                <p>
                    The Institute has long been operating with a less-than-ideal number of trustees. An amended charter would have enabled the Institute to better respond to the needs of Filipino rice farmers and consumers, and to the country’s efforts to attain food security.
                </p>
                <p>
                    There is an immediate need to strengthen the BOT and reconfigure the Institute’s powers and functions as stated in its present charter to further its purposes and objectives and make its processes and program implementation more efficient and cost-effective.
                </p>
            </div>
        </div>
