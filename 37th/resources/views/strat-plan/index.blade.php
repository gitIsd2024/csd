                    <p>Matatag, maginhawa, at panatag na buhay (firmly rooted, comfortable, and secure life) – this is the collective vision of the kind of life we want every Filipino to live (AmBisyon Natin 2040). Food security and poverty alleviation are among the primary components of this societal goal, where no Filipino is ever poor and hungry. While we have achieved great strides toward fulfilling these goals, our progress has encountered setbacks, particularly with the COVID-19 global pandemic. As our nation works to recover fast from the lingering scars of the plague, we need to consolidate and strengthen our food security efforts once again to get us back on track and seize the opportunity to build back better.</p>

                    <p>Rice – our staple food – remains at the front and center of our food security concerns. While the rice industry recorded high production in 2020 and 2021, perennial challenges continue to hound the industry players. A great number of rice farmers are still poor and afflicted with low productivity and high cost of production. War-caused disruptions in the global food supply chain exacerbate the situation by making agricultural inputs such as fertilizer and fuel more expensive.</p>

                    <p>Oftentimes, farmers have no other options but sell their fresh palay at low prices. Small farm sizes also impede many of the farmers’ scaling opportunities. Frequent natural calamities associated with changing climate increase their production risks and minimize their livelihood options. Weak institutions, such as fragmented agricultural extension systems and unclear roles among the national agencies and local government units, make it more difficult for farmers to access agriculture services.</p>

                    <p>As the country transitioned to a more liberal international trade, rice market players face stiff competition with better quality and cheaper imports, and sometimes smuggled rice. On the demand side, rising food prices bring down consumers’ purchasing power, eventually leading to lower food intake and higher malnutrition. If not addressed decisively, all these difficulties will derail the attainment of the eight-point economic agenda that the government has set to attain by 2028.</p>
                </div>
                    <p>No less than President Ferdinand R. Marcos, Jr. recognized the urgency of the matter when he declared that more than being a trade commodity or a source of livelihood, food is an existential and moral imperative on which the survival of any society depends. Noting the many things in the agriculture sector that would need immediate support, the President proposes a long-term solution to raise agricultural production by strengthening the value chain from farmers to consumers. He also acknowledged the role of research in the process.</p>
                
                    <p>As a relevant R&D institution, DA-PhilRice has been helping increase the productivity and income of farmers. In 2018-2022, the Institute bred 28 new rice varieties, developed integrated crop management decision support tools, and designed efficient farm machines. Along with our partners, we also secured the approval for the commercial propagation of Golden Rice, a new type of nutritious rice that contains beta-carotene. The Institute has also built the Philippine Rice Information System, which uses satellite imagery and other new digital technologies to generate information on planted rice areas, seasonality, and yield, and to predict crop risks.</p>
                
                    <p>Through its pivotal roles in implementing the Rice Competitiveness Enhancement Fund (RCEF) Seed and Rice Extension Services Programs, we also helped farmers gain greater access to high-quality seeds and learning materials resulting in yield improvement in target provinces. To increase farmers’ income through better market participation, we also engaged them in agribusiness through our Rice Business Innovations System (RiceBIS) program and “Sa Palay at Gulay may Ani, Hanapbuhay, Oportunidad at Nutrisyon” (PAG-AHON) project.</p>
                
                    <p>In the process, we understood that the pathway to achieving our desired impact is to implement client-centric strategies in our research for development and extension (R4DE) operations. This means that we will focus on our clients’ needs, preferences, and values to build a close and productive relationship (Figures 1 & 2). We also acknowledge that technology development is merely a means to an end; that for science to serve a bigger cause, its product must reach the end-users and serve their purpose. R4D is not enough – extension is a must to ensure that our stakeholders are reached through their preferred means of communication and interaction. We will leverage digital transformation to respond efficiently to both our internal and external stakeholders, improve our human resource productivity, and establish better data quality, accessibility, and security.</p>

                    <p>With this expanded paradigm of R4DE, while recognizing present conditions and building on the achievements gained and lessons learned from the immediate past, DA-PhilRice is now poised as presented in this new Strategic Plan 2023-2028.</p>
            </div>
            
