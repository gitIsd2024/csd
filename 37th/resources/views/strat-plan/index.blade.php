            <div class="section-title">
              Executive Summary
            </div>
            <div class="content-section">
              <div class="highlight">
                <p>The government hopes that all Filipinos will have a comfortable and secure life. That is why it is our goal to protect the purchasing power of families through food security - the first among the eight-point economic agenda of President Ferdinand R. Marcos Jr. under the Philippine Development Plan 2023-2028.</p>
              </div>
              <p>To pursue this goal, the Institute endeavors to build back better by standing on the achievements gained and lessons learned from the past. Toward a vision of “Advanced science and technology for prosperous rice communities toward sufficient and affordable rice for all”, the Institute shall advance climate-smart, socially inclusive, demand-driven, and partnership-based R4DE.</p>
              <p>This Strategic Plan 2023-2028 is distinguished by its shift from a commodity-focused to a people-centric outlook. This shift is only logical given that rice farming is the major source of livelihood in the rural areas and more than two million Filipino families derive half of their household income from rice cultivation. In addition, millions of landless farm workers, and tens of thousands of merchants indirectly depend on rice for a living. Thus, this Plan recognizes that the prosperity of farmers and their communities is key to having sufficient and affordable rice for the country.</p>

              <p>Hence, aligned with and guided by the DA Strategic Agenda (2023-2028), Ambisyon Natin 2040, the National Agriculture and Fisheries Modernization and Industrialization Plan (NAFMIP) 2021-2030, and the Sustainable Development Goals, DA-PhilRice will implement three new flagship R4DE programs:</p>
              <ul>
                <li><strong>Rice Seeds Systems (RSS)</strong> involves improving and sustaining vigorous seed supply chains by developing innovative approaches toward seed security – from seed availability, access, and utilization to provide farmers with their preferred seeds that are high-quality;</li>
                <li><strong>Scaling Modern and Adaptive Rice Technologies for Prosperous Farming Communities (SMART Farm)</strong> focuses on innovating rice farming systems to help farmers boost their yield and income through the development of cutting-edge climate-resilient technologies in the whole value chain to increase the quantity and quality of rice which will ensure the availability and accessibility of nutritious, balanced, and safe rice and rice-based diets to all Filipinos at all times;</li>
                <li><strong>Rice Business Innovations System 2.0 (RiceBIS 2.0)</strong> encompasses improving the production to marketing of farmers’ groups with the goal of transforming rice-based farming communities into inclusive, competitive, and sustainable agro-entrepreneurs.</li>
              </ul>
            </div>

            <div class="content-section">
              <h3 class="section-header">Strategic Plan Anchors</h3>
              <p>The new Strategic Plan is also anchored on upscaling, digital transformation, and partnership. Upscaling is crucial since we want to ensure that our programs and technologies will result in adoption and significantly impact more communities nationwide. Digital transformation will integrate all our technologies into one system to prevent confusion and duplication, and make our platforms more efficient in answering the needs of our farmers. The Institute understands that it cannot deliver its desired impact by itself, hence it partners with farmers, SUCs, civil society, the private sector, and other stakeholders within the agriculture value chain to create a whole-of-nation approach to work strongly toward improving food production.</p>
            </div>

            <div class="content-section">
              <h3 class="section-header">Goals and Targets</h3>
              <p>Along with the Institute’s special programs on RCEF Seed and Extension and Malusog Rice, and the discipline- and area-based projects, these flagship programs aim to contribute to having:</p>
              <ol>
                <li>Prosperous and empowered rice communities,</li>
                <li>Nutrition-secured Filipinos, and</li>
                <li>A resilient and sustainable rice industry by the end of 2028.</li>
              </ol>
              <p>DA-PhilRice shall specifically work to help improve the productivity and income of rice farmers through the following:</p>
              <ul>
                <li>Increasing yield by 4% to 6% per year;</li>
                <li>Reducing production cost by 38-45%;</li>
                <li>Minimizing postharvest losses to 12%;</li>
                <li>Diversifying sources of farm income leading to bigger income from other agriculture sources by 100%; and</li>
                <li>Engaging in value-adding activities and rice-based enterprises resulting in 50% increase in rice income.</li>
              </ul>
              <p>These are expected to help realize the goals of the DA Strategic Agenda (2023-2028), particularly the boosting of local production and raising the farmer’s and fisherfolk’s income (agenda 1); ensuring accessibility to affordable, safe, and nutritious food (agenda 2); developing strong, modernized, and climate-resilient value chains (agenda 3); and institutionalizing policy reforms and strengthening institutions (agenda 4).</p>
            </div>


